function getGlyph(charCode: number): number[] {
  return FONT_8X16[charCode] ?? FONT_8X16[32]!;
}

/**
 * Render a single text line to scaled raster rows.
 * Each font pixel becomes a SCALE x SCALE block of dots.
 */
function renderTextRow(text: string, align: 'left' | 'center' | 'right', bold: boolean): Buffer[] {
  const textWidth = text.length * SCALED_CHAR_WIDTH;
  let offsetPx: number;

  switch (align) {
    case 'center':
      offsetPx = Math.max(0, Math.floor((PRINT_WIDTH_PIXELS - textWidth) / 2));
      break;
    case 'right':
      offsetPx = Math.max(0, PRINT_WIDTH_PIXELS - textWidth);
      break;
    default:
      offsetPx = 0;
  }

  const rows: Buffer[] = [];

  for (let fontRow = 0; fontRow < CHAR_HEIGHT; fontRow++) {
    // Each font row is repeated SCALE times vertically
    const line = Buffer.alloc(BYTES_PER_LINE, 0x00);

    for (let charIdx = 0; charIdx < text.length; charIdx++) {
      const glyph = getGlyph(text.charCodeAt(charIdx));
      const glyphByte = glyph[fontRow] ?? 0;

      for (let fontCol = 0; fontCol < CHAR_WIDTH; fontCol++) {
        if ((glyphByte >> (7 - fontCol)) & 1) {
          // Scale horizontally: each font pixel becomes SCALE dots
          for (let sx = 0; sx < SCALE; sx++) {
            const px = offsetPx + charIdx * SCALED_CHAR_WIDTH + fontCol * SCALE + sx;
            if (px < PRINT_WIDTH_PIXELS) {
              line[Math.floor(px / 8)]! |= 1 << (7 - (px % 8));
            }

            // Bold: shift right by 1 extra pixel
            if (bold) {
              const bpx = px + 1;
              if (bpx < PRINT_WIDTH_PIXELS) {
                line[Math.floor(bpx / 8)]! |= 1 << (7 - (bpx % 8));
              }
            }
          }
        }
      }
    }

    // Scale vertically: repeat each row SCALE times
    for (let sy = 0; sy < SCALE; sy++) {
      rows.push(Buffer.from(line));
    }
  }

  return rows;
}

/** Solid horizontal rule, 2 dots tall */
function renderDivider(): Buffer[] {
  const line = Buffer.alloc(BYTES_PER_LINE, 0x00);
  for (let px = 4; px < PRINT_WIDTH_PIXELS - 4; px++) {
    line[Math.floor(px / 8)]! |= 1 << (7 - (px % 8));
  }
  return [
    Buffer.alloc(BYTES_PER_LINE, 0x00),
    Buffer.from(line),
    Buffer.from(line),
    Buffer.alloc(BYTES_PER_LINE, 0x00),
  ];
}

function blankLines(count: number): Buffer[] {
  const rows: Buffer[] = [];
  for (let i = 0; i < count; i++) {
    rows.push(Buffer.alloc(BYTES_PER_LINE, 0x00));
  }
  return rows;
}

/** Scale a 1-bit bitmap by a factor in both dimensions */
function scaleBitmap(
  data: Buffer,
  width: number,
  height: number,
  factor: number,
): { data: Buffer; width: number; height: number } {
  const srcBytesPerRow = Math.ceil(width / 8);
  const newWidth = width * factor;
  const newHeight = height * factor;
  const dstBytesPerRow = Math.ceil(newWidth / 8);
  const out = Buffer.alloc(dstBytesPerRow * newHeight, 0x00);

  for (let y = 0; y < height; y++) {
    for (let x = 0; x < width; x++) {
      const srcByteIdx = y * srcBytesPerRow + Math.floor(x / 8);
      const srcBitIdx = 7 - (x % 8);
      if ((data[srcByteIdx]! >> srcBitIdx) & 1) {
        for (let sy = 0; sy < factor; sy++) {
          for (let sx = 0; sx < factor; sx++) {
            const dx = x * factor + sx;
            const dy = y * factor + sy;
            const dstByteIdx = dy * dstBytesPerRow + Math.floor(dx / 8);
            out[dstByteIdx]! |= 1 << (7 - (dx % 8));
          }
        }
      }
    }
  }

  return { data: out, width: newWidth, height: newHeight };
}

/** Embed a pre-rendered 1-bit bitmap (from BarcodeRenderer) centered on the print width */
function embedBitmap(data: Buffer, width: number, height: number): Buffer[] {
  const srcBytesPerRow = Math.ceil(width / 8);
  const offsetPx = Math.max(0, Math.floor((PRINT_WIDTH_PIXELS - width) / 2));
  const rows: Buffer[] = [];

  for (let y = 0; y < height; y++) {
    const line = Buffer.alloc(BYTES_PER_LINE, 0x00);
    for (let x = 0; x < width; x++) {
      const srcByteIdx = y * srcBytesPerRow + Math.floor(x / 8);
      const srcBitIdx = 7 - (x % 8);
      if ((data[srcByteIdx]! >> srcBitIdx) & 1) {
        const dstPx = offsetPx + x;
        if (dstPx < PRINT_WIDTH_PIXELS) {
          line[Math.floor(dstPx / 8)]! |= 1 << (7 - (dstPx % 8));
        }
      }
    }
    rows.push(line);
  }

  return rows;
}

/**
 * Render a full receipt to raster line buffers.
 * Each buffer is BYTES_PER_LINE (72) bytes = one dot row of 576 pixels.
 */
export async function renderReceipt(data: ReceiptData): Promise<Buffer[]> {
  const rows: Buffer[] = [];

  for (const line of data.lines) {
    switch (line.type) {
      case 'text': {
        if (line.content.length === 0) {
          rows.push(...blankLines(SCALED_CHAR_HEIGHT / 2));
        } else {
          rows.push(...renderTextRow(line.content, line.align ?? 'left', line.bold ?? false));
          rows.push(...blankLines(LINE_GAP));
        }
        break;
      }
      case 'divider': {
        rows.push(...renderDivider());
        break;
      }
      case 'feed': {
        const feedDots = (line.lines ?? 1) * (SCALED_CHAR_HEIGHT / 2);
        rows.push(...blankLines(feedDots));
        break;
      }
      case 'barcode': {
        const opts: BarcodeRenderOptions = {
          dpi: BARCODE_DPI,
          moduleHeight: 20,
          includeText: true,
        };
        const barcode = await renderBarcode(line.value, line.format ?? 'CODE128', opts);
        // Scale 2x in both directions for scannable barcode
        const scaled = scaleBitmap(barcode.data, barcode.width, barcode.height, 2);
        rows.push(...blankLines(8));
        rows.push(...embedBitmap(scaled.data, scaled.width, scaled.height));
        rows.push(...blankLines(8));
        break;
      }
      case 'qr': {
        const qr = await renderQR(line.value, { dpi: BARCODE_DPI });
        rows.push(...blankLines(8));
        rows.push(...embedBitmap(qr.data, qr.width, qr.height));
        rows.push(...blankLines(8));
        break;
      }
    }
  }

  return rows;
}

